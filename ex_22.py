# Zadanie 22: Wielkie i małe litery

# Zapytanie użytkownika o tekst
tekst = input("Podaj tekst: ")

# Wyświetlenie tekstu wielkimi literami
print("Tekst wielkimi literami:", tekst.upper())

# Wyświetlenie tekstu małymi literami
print("Tekst małymi literami:", tekst.lower())

# Wyświetlenie tekstu z pierwszą literą wielką
print("Tekst z pierwszą literą wielką:", tekst.capitalize())
