# Zadanie 18: Zbiory (set)

# Utworzenie zbiorów
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

# Wyświetlenie sumy zbiorów
print("Suma zbiorów A i B:", A | B)

# Wyświetlenie przecięcia zbiorów
print("Przecięcie zbiorów A i B:", A & B)

# Wyświetlenie różnicy zbiorów A - B
print("Różnica zbiorów A - B:", A - B)
